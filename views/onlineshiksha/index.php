function send_notify(){
  var name = $("#name").val().trim();
  var phone = $("#phone").val().trim();
  var email = $("#email").val().trim();
  var email_pattern = /^[a-z0-9._%+-]+@[a-z0-9.-]+\.[a-z]{2,4}$/;
  if(name=="")
  {
    $("#err_name").fadeIn().html("Please enter name").css('color','red');
        setTimeout(function(){$("#err_name").html("");},3000);
        $("#name").focus();
        return false;
    }
    if(phone=="")
    {
        $("#err_mobile").fadeIn().html("Please enter Mobile No.").css('color','red');
        setTimeout(function(){$("#err_mobile").html("");},3000);
        $("#phone").focus();
        return false;
    }
    else if(phone.length < 10)
    {
        $("#err_mobile").fadeIn().html("Invalid Mobile No.").css('color','red');
        setTimeout(function(){$("#err_mobile").html("");},3000);
        $("#phone").focus();
        return false;
    }

    if(email=="")
    {
        $("#err_email").fadeIn().html("Please enter email id").css('color','red');
        setTimeout(function(){$("#err_email").html("");},3000);
        $("#email").focus();
        return false;
    }
    if(!email_pattern.test(email))
    {
          $("#err_email").fadeIn().html("Invalid Email id").css('color','red');
          setTimeout(function(){$("#err_email").html("");},3000);
          $("#email").focus();
          return false;
    }
    $("#submit").html('<i class="fa fa-spinner fa-pulse fa-1x fa-fw"></i>');
    $.ajax({
        type : 'post',
        cache : false,
        url : "<?php echo base_url();?>category/notify/",
        data : {name : name, phone : phone, email : email },
        success : function(data){
            $("#submit").html('JOIN');
            var contactform = $("#contactform").html();
            $("#contactform").html('<h2 style="padding:50px;text-align : center;">Thank You. we have sent you joining details on your mail.</h2>');
            setTimeout(function(){$("#contactform").html(contactform);
              window.open('https://zoom.us/j/95152726510?pwd=UmQ1RjZuUG1LQUUxckxBQ2pqcEo5UT09');
          },3000);

            // 
        }
    });
}
